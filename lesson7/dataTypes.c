/**
 * Data types : on distingue deux groupes de données :
 * Les données primitive (ou atomiques) et les données complexes.
 * 
 * Les données primitives sont les "plus petites" données possibles (ex : un byte).
 * "Petit" ne correspond pas à la taille ou au poids de la donnée, cela signifie juste que cette donnée est indivisible.
 * 
 * Les données complexes seraient ainsi des structures construites avec les données primitives (ex : une chaîne de caractères).
 * 
 * On peut ainsi faire le parallèle avec des légos (primitive = légo, complexe = construction en légo)
 */
